@dataclass
class Example:
    global_context: np.ndarray
    hand_cards: np.ndarray
    hand_mask: np.ndarray
    jokers: np.ndarray
    joker_mask: np.ndarray
    joker_ids: np.ndarray
    copy_active: np.ndarray
    copy_target_ids: np.ndarray
    trigger_match: np.ndarray
