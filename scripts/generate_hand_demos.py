def _pad_1d(arr: np.ndarray, max_n: int, dtype: type) -> np.ndarray:
    out = np.zeros(max_n, dtype=dtype)
    n = min(len(arr), max_n)
    out[:n] = arr[:n]
    return out


def _pad_entities(arr: np.ndarray, max_n: int, dim: int) -> tuple[np.ndarray, np.ndarray]:
    n = arr.shape[0]
    if n > max_n:
        raise GenerationError(f"entity count {n} exceeds max {max_n}")
    padded = np.zeros((max_n, dim), dtype=np.float32)
    mask = np.zeros(max_n, dtype=bool)
    if n > 0:
        padded[:n] = arr
        mask[:n] = True
    return padded, mask


def acted_cards_from_choice(choice: AnteClearChoice) -> list:
    """The cards the label's action actually operates on.

    NOT simply `choice.hold` -- `hold`/`discard` are named from the
    "keep vs. discard" framing of a *discard* decision. For `action=="play"`,
    `hold` is repurposed by `hand_solver.py` to mean "the subset chosen to
    play" (see `best_immediate_play`'s callers), not "cards kept in hand".
    Getting this backwards silently produces a Discard label for what the
    solver actually recommended as a Play, or vice versa.
    """
    if choice.action == "play":
        return choice.hold
    if choice.action == "discard":
        return choice.discard
    raise GenerationError(f"unknown solver action: {choice.action!r}")


def indices_by_identity(cards: list, hand: list) -> list[int]:
    """Map `cards` back to their positions in `hand` by object identity,
    not value equality. `Card` is a plain @dataclass (value-based `__eq__`),
    so an Erratic deck's duplicate-valued cards would otherwise collide
    under `in`/`.index()` -- see the /grilling session and the matching fix
    in `hand_solver.best_immediate_play`.
    """
    id_to_index = {id(c): i for i, c in enumerate(hand)}
    try:
        return [id_to_index[id(c)] for c in cards]
    except KeyError as exc:
        raise GenerationError(
            f"solver returned a card not present (by identity) in the original hand: {exc}"
        ) from exc


class HandTurnState(Protocol):
    """The minimal surface the solve/encode body needs from a hand-turn state.

    Both sources satisfy it: `HandPlayAdapter` (stages 1-4, domain-randomized
    sampling) and `RestoredHarvestState` (stage 5, a restored harvest blob).
    Keeping the body written against this protocol rather than against
    `HandPlayAdapter` is what lets harvested states flow through the EXACT same
    solver, encoder and executability checks -- a second labeling path would be
    a label-divergence surface, which is the bug class this pipeline keeps
    getting bitten by.
    """

    @property
    def raw_state(self) -> dict[str, Any]: ...

    def step(self, action: Any) -> Any: ...


class RestoredHarvestState:
    """A harvested blob, restored, presented as a `HandTurnState`.

    Deliberately thin: it exists only so `label_and_encode` can step the engine
    for tier-2 validation the same way `HandPlayAdapter` does. Restoration and
    capture-skew repair happen in `harvest_restore.restore_state` -- never
    unpickle a harvest blob directly (see that module's docstring).
    """

    def __init__(self, gs: dict[str, Any]) -> None:
        self._gs = gs

    @property
    def raw_state(self) -> dict[str, Any]:
        return self._gs

    def step(self, action: Any) -> dict[str, Any]:
        from jackdaw.engine.game import step as engine_step

        engine_step(self._gs, action)
        return self._gs


def validate_label_executability(
    state: HandTurnState,
    action_type: int,
    selected_indices: list[int],
) -> None:
    """Two-tier guard that a label is executable in the real game. Added
    after the 5-card-discard-cap bug (see CLAUDE.md): 8.3% of the first
    stage-1 dataset labeled discards the engine rejects, and it wasn't
    caught until BC training consumed the shards. Cost is negligible
    against the 2-12s solver call (~10us mask check + ~0.5ms engine step).

    Tier 1: schema-native validation. Indices must be a canonical ascending
    set of 1--5 positions within this state's actual hand length, and the
    action's hand/discard budget must remain. This intentionally does not
    route through the old fixed-width Discrete(436) action space: doing so
    silently rejected every label touching position 8 or later.

    Tier 2: actually execute the action through the real engine. This is
    DESTRUCTIVE to the adapter's state -- call it only after all
    observation encoding is done (the state is discarded after the example
    anyway). Exercises the engine's own validator plus the full
    scoring/joker path, i.e. the exact code path `HandPlayGymEnv` runs
    during PPO.

    Raises `GenerationError` on any failure -- per the pipeline's design,
    the worker logs it to failures.jsonl and skips the example.
    """
    gs = state.raw_state
    cr = gs.get("current_round", {})
    hand_size = len(gs.get("hand", []))
    if action_type not in (ActionType.PlayHand, ActionType.Discard):
        raise GenerationError(f"unknown action_type={action_type}")
    if not 1 <= len(selected_indices) <= MAX_LABEL_CARDS:
        raise GenerationError("label must select 1-5 cards")
    if selected_indices != sorted(selected_indices) or len(set(selected_indices)) != len(
        selected_indices
    ):
        raise GenerationError("label indices must be unique and sorted ascending")
    if any(index < 0 or index >= hand_size for index in selected_indices):
        raise GenerationError(
            f"label indices {selected_indices} outside actual hand length {hand_size}"
        )
    if action_type == ActionType.PlayHand and cr.get("hands_left", 0) < 1:
        raise GenerationError("PlayHand label is illegal with no hands left")
    if action_type == ActionType.Discard and cr.get("discards_left", 0) < 1:
        raise GenerationError("Discard label is illegal with no discards left")

    indices = tuple(selected_indices)
    engine_action = (
        EnginePlayHand(card_indices=indices)
        if action_type == ActionType.PlayHand
        else EngineDiscard(card_indices=indices)
    )
    try:
        state.step(engine_action)
    except Exception as exc:
        raise GenerationError(f"label failed engine execution: {exc}") from exc


def generate_one_example(seed: str, config: HandPlayConfig) -> Example:
    """Sample one domain-randomized hand-play state via `HandPlayAdapter`
    and label it with the exact P(clear)-not-EV solver. Raises
    `GenerationError` (or lets a solver exception propagate) on any
    failure -- callers must catch and log, not swallow silently.
    """
    adapter = HandPlayAdapter(config)
    adapter.reset(BACK_KEY, STAKE, seed)
    return label_and_encode(adapter, seed)


def generate_one_example_from_blob(blob: bytes, record_id: str) -> Example:
    """Label one HARVESTED state (C2): restore the blob, then run the exact
    same solve/encode body stages 1-4 use.

    `record_id` doubles as the `mc_seed`, so a harvested label is byte-
    reproducible across machines. It is the record id -- NOT the run seed --
    because two records from one run must not share MC draws (pitfall #6).

    Restoration goes through `harvest_restore.restore_state`, which repairs
    known capture skew; unpickling the blob directly here would label states
    under an engine bug that no longer exists.
    """
    from harvest_restore import restore_state

    return label_and_encode(RestoredHarvestState(restore_state(blob)), record_id)


def label_and_encode(state: HandTurnState, seed: str) -> Example:
    """Solve, label and encode one hand-turn state, whatever produced it.

    THE single labeling path: stages 1-4 reach it with a freshly sampled
    `HandPlayAdapter` state, stage 5 with a restored harvest blob. `seed`
    threads into the solver as `mc_seed` and is stored on the Example.
    """
    gs = state.raw_state

    hand: list = gs["hand"]
    jokers: list = gs["jokers"]
    hand_levels = gs["hand_levels"]
    blind = gs["blind"]
    rng = gs["rng"]
    cr = gs["current_round"]

    hands_left = cr.get("hands_left", 0)
    discards_left = cr.get("discards_left", 0)
    blind_chips = getattr(blind, "chips", 0) if blind else 0
    chips_needed = max(0.0, blind_chips - gs.get("chips", 0))

    deck = DeckComposition.from_deck(gs.get("deck", []))
    flags = get_hand_eval_flags(jokers)

    choice = solve_hand_for_ante_clear(
        hand,
        jokers,
        hand_levels,
        blind,
        rng,
        deck,
        chips_needed,
        hands_left,
        discards_left,
        game_state=gs,
        blind_chips=blind_chips,
        four_fingers=flags["four_fingers"],
        shortcut=flags["shortcut"],
        mc_seed=seed,  # makes the future-hand MC draws (and p_clear) reproducible
    )

    acted_cards = acted_cards_from_choice(choice)
    selected_indices = sorted(indices_by_identity(acted_cards, hand))

    action_type = ActionType.PlayHand if choice.action == "play" else ActionType.Discard

    # Dual counter, matching build_observation_v2: a GENUINE overfill
    # (non-negative jokers exceeding joker_slots) is a Riff-raff-class engine
    # bug and stays loud; a legal wide/negative build (negatives don't consume
    # a slot) is ENCODED, not dropped. Without this the harvest labeler would
    # raise GenerationError on every >5-joker state and silently skip it to
    # failures.jsonl -- exactly the negative/wide builds the h1 model should
    # value.
    _check_joker_overfill(gs, jokers)
    ent = encode_hand_state_v2(gs)

    # Hand rows deliberately stay actual-width. write_shard pads each shard
    # only to its widest example, then the loader up-pads to the obs width.
    # Jokers/consumables truncate their tail past the fixed v2 width (harvested
    # full-run states can exceed it via negative editions -- Negative jokers
    # for the joker block, Perkeo copies for the consumable block); truncation
    # is lowest-slot-first as a pure safety valve, same policy as
    # build_observation_v2.
    joker_padded, joker_mask = _pad_entities(
        ent["jokers"][:MAX_JOKERS_V2], MAX_JOKERS_V2, D_JOKER
    )
    cons_padded, cons_mask = _pad_entities(
        ent["consumables"][:MAX_CONSUMABLES_V2], MAX_CONSUMABLES_V2, D_CONSUMABLE
    )
    trigger = np.zeros((len(hand), MAX_JOKERS_V2, 2), dtype=bool)
    trigger_src = ent["trigger_match"][:, :MAX_JOKERS_V2]
    trigger[: trigger_src.shape[0], : trigger_src.shape[1]] = trigger_src

    # Must come AFTER encoding: tier 2 executes the action on the real
    # state, mutating it (the state is discarded after this function).
    validate_label_executability(state, action_type, selected_indices)

    return Example(
        global_context=ent["global_context"],
        hand_cards=ent["hand_cards"],
        hand_mask=np.ones(len(hand), dtype=bool),
        jokers=joker_padded,
        joker_mask=joker_mask,
        joker_ids=_pad_1d(ent["joker_ids"], MAX_JOKERS_V2, np.int64),
        copy_active=_pad_1d(ent["copy_active"], MAX_JOKERS_V2, np.float32),
        copy_target_ids=_pad_1d(ent["copy_target_ids"], MAX_JOKERS_V2, np.int64),
        trigger_match=trigger,
        consumables=cons_padded,
        consumable_mask=cons_mask,
        action_type=int(action_type),
        card_indices=np.pad(
            np.asarray(selected_indices, dtype=np.int64),
            (0, MAX_LABEL_CARDS - len(selected_indices)),
            constant_values=-1,
        ),
        p_clear=float(choice.p_clear),
        seed=seed,
    )


def write_shard(path: Path, examples: list[Example]) -> None:
    """Write one `.npz` shard. `schema_version` lets a future consumer
    detect stale data if `observation.py`'s encoding shape ever changes.

    The consumable block stores the REAL owned consumables (empty for
    stages 1-4, genuinely populated for harvested C2 states) while the
    LABELS stay consumable-blind: the solver ignores consumables entirely,
    so `p_clear`/action labels are computed as if the block were empty. BC
    just learns an input that is inert for hand-play -- deliberately, so
    h2's BC pool doesn't show zeros where live play shows real consumables
    (in-blind consumable SELECTION itself stays at h2; see
    docs/pre-regen-handoff.md, B2 slice 4 rider).
    """
    hand_width = max(example.hand_cards.shape[0] for example in examples)

    def pad_hand_axis(arr: np.ndarray) -> np.ndarray:
        padded = np.zeros((hand_width, *arr.shape[1:]), dtype=arr.dtype)
        padded[: arr.shape[0]] = arr
        return padded

    np.savez_compressed(
        path,
        schema_version=np.array([SCHEMA_VERSION]),
        global_context=np.stack([e.global_context for e in examples]),
        hand_cards=np.stack([pad_hand_axis(e.hand_cards) for e in examples]),
        hand_mask=np.stack([pad_hand_axis(e.hand_mask) for e in examples]),
        jokers=np.stack([e.jokers for e in examples]),
        joker_mask=np.stack([e.joker_mask for e in examples]),
        joker_ids=np.stack([e.joker_ids for e in examples]),
        copy_active=np.stack([e.copy_active for e in examples]),
        copy_target_ids=np.stack([e.copy_target_ids for e in examples]),
        trigger_match=np.stack([pad_hand_axis(e.trigger_match) for e in examples]),
        consumables=np.stack([e.consumables for e in examples]),
        consumable_mask=np.stack([e.consumable_mask for e in examples]),
        action_type=np.array([e.action_type for e in examples], dtype=np.int64),
        card_indices=np.stack([e.card_indices for e in examples]),
        p_clear=np.array([e.p_clear for e in examples], dtype=np.float32),
        seed=np.array([e.seed for e in examples]),
    )


def _resume_point(
    output_dir: Path, worker_id: int, start_idx: int, end_idx: int
) -> tuple[int, int]:
    """Scan this worker's existing shards and return (resume_idx, next_shard_idx).

    Each stored example's seed string ends in its global index, so an
    interrupted run can be relaunched with the SAME stage/total/num-workers
    settings and each worker continues after the highest index it already
    wrote (killing a run only ever costs the current in-memory buffer).
    Seeds outside this worker's [start_idx, end_idx) range are ignored —
    they'd mean the partitioning changed, in which case shard files no
    longer correspond to ranges and resuming would silently skip or
    duplicate work. Failed (skipped) indices before the resume point are
    already in the failures log and are not retried.
    """
    resume_idx = start_idx
    shard_paths = sorted(output_dir.glob(f"worker_{worker_id:03d}_shard_*.npz"))
    for shard_path in shard_paths:
        seeds = np.load(shard_path)["seed"]
        for seed in seeds:
            idx = int(str(seed).rsplit("_", 1)[-1])
            if start_idx <= idx < end_idx:
                resume_idx = max(resume_idx, idx + 1)
    return resume_idx, len(shard_paths)


def _worker_run(
    worker_id: int,
    start_idx: int,
    end_idx: int,
    stage_name: str,
    config: HandPlayConfig,
    output_dir: Path,
    shard_size: int,
) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    failures_path = output_dir / f"worker_{worker_id:03d}_failures.jsonl"

    resume_idx, shard_idx = _resume_point(output_dir, worker_id, start_idx, end_idx)

    buffer: list[Example] = []

    def flush() -> None:
        nonlocal shard_idx
        if not buffer:
            return
        shard_path = output_dir / f"worker_{worker_id:03d}_shard_{shard_idx:05d}.npz"
        write_shard(shard_path, buffer)
        shard_idx += 1
        buffer.clear()

    with open(failures_path, "a", encoding="utf-8") as failures_file:
        for global_idx in range(resume_idx, end_idx):
            seed = f"{stage_name}_{global_idx:08d}"
            try:
                example = generate_one_example(seed, config)
            except Exception as exc:  # noqa: BLE001 -- one bad sample must not kill the worker
                failures_file.write(
                    json.dumps(
                        {
                            "seed": seed,
                            "error": str(exc),
                            "traceback": traceback.format_exc(),
                        }
                    )
                    + "\n"
                )
                failures_file.flush()
                continue
            buffer.append(example)
            if len(buffer) >= shard_size:
                flush()
        flush()


def partition_indices(total: int, num_workers: int) -> list[tuple[int, int]]:
    """Split [0, total) into up to `num_workers` contiguous, non-overlapping
    (start, end) ranges covering every index exactly once. The range
    boundaries depend only on (total, num_workers) -- not on wall-clock
    scheduling -- which is what makes the seed set reproducible.
    """
    if total <= 0 or num_workers <= 0:
        return []
    chunk = -(-total // num_workers)  # ceil division
    ranges = []
    for worker_id in range(num_workers):
        start_idx = worker_id * chunk
        end_idx = min(start_idx + chunk, total)
        if start_idx >= end_idx:
            break
        ranges.append((start_idx, end_idx))
    return ranges


def run_generation_job(job: GenerationJobConfig) -> None:
    """Top-level entry point: partitions [0, total_examples) into fixed
    per-worker index ranges and spawns one independent process per worker.
    Each worker writes its own shards and failures file -- no results are
    passed back through this process.
    """
    job.output_dir.mkdir(parents=True, exist_ok=True)

    manifest = {
        "stage_name": job.stage_name,
        "schema_version": SCHEMA_VERSION,
        "total_examples": job.total_examples,
        "num_workers": job.num_workers,
        "shard_size": job.shard_size,
        "back_key": BACK_KEY,
        "stake": STAKE,
        "hand_play_config": asdict(job.hand_play_config),
        "generated_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
    }
    with open(job.output_dir / "manifest.json", "w", encoding="utf-8") as f:
        json.dump(manifest, f, indent=2)

    processes = []
    for worker_id, (start_idx, end_idx) in enumerate(
        partition_indices(job.total_examples, job.num_workers)
    ):
        p = multiprocessing.Process(
            target=_worker_run,
            args=(
                worker_id,
                start_idx,
                end_idx,
                job.stage_name,
                job.hand_play_config,
                job.output_dir,
                job.shard_size,
            ),
        )
        p.start()
        processes.append(p)

    for p in processes:
        p.join()


