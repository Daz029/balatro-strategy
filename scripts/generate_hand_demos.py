def all_joker_keys() -> tuple[str, ...]:
    """Every joker key in the engine's centers.json (150 as of writing)."""
    centers_path = _REPO_ROOT / "jackdaw" / "engine" / "data" / "centers.json"
    with open(centers_path, encoding="utf-8") as f:
        centers = json.load(f)
    return tuple(sorted(k for k in centers if k.startswith("j_")))


